package datastructure.array.offer;

public class Java56_02_NumberAppearingOnce {

    public static int once(int[] ints) {
        return -1;
    }

    public static void main(String[] args) {
        int[] ints = {1, 2, 2, 2};
        System.out.println("value-" + once(ints) + "; target-1");
    }
}
